package com.example.shaharproject;

public class exercises
{
    private String id;

    private String exercisecode;

    private String exercisedate;

    private String starthour;

    private String place;

    private String trainercode;

    private String notes;

    public exercises()
    {

    }

    public String getId() {return this.id;}

    public void setId(String id) {this.id=id;}

    public String getExercisecode() {return this.exercisecode;}

    public void setExercisecode(String exercisecode) {this.exercisecode=exercisecode;}

    public String getExercisedate() {return this.exercisedate;}

    public void setExercisedate(String exercisedate) {this.exercisedate=exercisedate;}

    public String getStarthour() {return this.starthour;}

    public void setStarthour(String starthour) {this.starthour=starthour;}

    public String getPlace() {return this.place;}

    public void setPlace(String place) {this.place=place;}

    public String getTrainercode() {return this.trainercode;}

    public void setTrainercode(String trainercode) {this.trainercode=trainercode;}

    public String getNotes() {return this.notes;}

    public void setNotes(String notes) {this.notes=notes;}
}
